    private void JbuttonSelecionarPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbuttonSelecionarPDFActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(false);
        
        if( chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION)
        {
         this.arquivoSelecionado = chooser.getSelectedFile();
         
         this.jLabelArquivoSelecionado.setText(this.arquivoSelecionado.getName());
        }
         
         
        
        
    }//GEN-LAST:event_JbuttonSelecionarPDFActionPerformed

    private void jButtonConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConverterActionPerformed
        
        if(this.arquivoSelecionado == null ||  !this.arquivoSelecionado.isFile())
        {
            JOptionPane.showMessageDialog(this, "Nenhum arquivo Selecionado!");
            return;
        }
        
        JFileChooser salvar = new JFileChooser();
        salvar.setMultiSelectionEnabled(false);
        salvar.setSelectedFile(new File("Arquivo.txt"));
        
        if( salvar.showSaveDialog(this) == JFileChooser.APPROVE_OPTION)
        {
                try
                {
                    File f = salvar.getSelectedFile();
                    this.conversor.parsePdf(this.arquivoSelecionado.getAbsolutePath(), f.getAbsolutePath());
                    JOptionPane.showMessageDialog(this, "Arquivo convertido com sucesso!");
                }
                catch(IOException e)
                {
                    JOptionPane.showMessageDialog(this, "NÃ£o foi possivel converter o arquivo erro informado:\n"+e.toString());
                }
        }
        
    }//GEN-LAST:event_jButtonConverterActionPerformed

